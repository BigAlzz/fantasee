# ── Seed suggestions ──────────────────────────────────────────────────
# Generate N distinct story seeds from a concept. The LLM returns a
# JSON array of {title, description, style, tone, characters} so the UI
# can show the user a picker. The selected seed(s) are then fed into the
# normal generation pipeline as a queue.

SEED_SYSTEM = """You are a creative writing assistant specializing in generating
distinct story seed ideas.

Given a concept, generate exactly N distinct story seeds. Each seed must have:

- "title": an evocative 2-6 word title (no quotes, no numbering)
- "description": a 1-2 sentence pitch — the hook that makes this story
  unique compared to the other seeds
- "style": visual style. Use one of: "fantasy painterly", "anime",
  "dark fantasy", "cinematic", "illustration". Vary across seeds when
  natural — don't repeat the same one for all N.
- "tone": one of: "dramatic", "dark", "epic", "mysterious", "lighthearted",
  "romantic", "melancholic", "hopeful", "suspenseful", "whimsical",
  "epic-fantasy", "noir", "lyrical", "gritty", "manhwa", "tense",
  "emotional". Vary across seeds.
- "characters": 1-2 sentence character list (comma-separated names +
  one-clause descriptors). Omit if the story doesn't need named
  characters.

Keep the seeds distinct — different angles, different settings, different
protagonists. Don't just rephrase the same idea N times.

Output a JSON array only. No prose before or after, no markdown fences."""


def _coerce_seed_item(raw: dict) -> dict:
    """Normalize one parsed seed into the shape the frontend expects."""
    title = (raw.get("title") or "").strip().strip('"').strip("'")
    description = (raw.get("description") or "").strip()
    style = (raw.get("style") or "fantasy painterly").strip().lower()
    tone = (raw.get("tone") or "dramatic").strip().lower()
    characters = (raw.get("characters") or "").strip()
    # Clamp to a reasonable title length so the picker cards stay readable
    if len(title) > 80:
        title = title[:77].rstrip() + "…"
    if len(description) > 320:
        description = description[:317].rstrip() + "…"
    return {
        "title": title or "Untitled Seed",
        "description": description,
        "style": style,
        "tone": tone,
        "characters": characters,
    }


def _parse_seed_response(text: str, expected: int) -> list[dict]:
    """Parse the LLM's JSON array response, with a couple of fallbacks
    for common formatting hiccups (trailing commas, fenced blocks, etc.).
    """
    import re
    candidate = (text or "").strip()

    # Strip markdown fences if present
    if candidate.startswith("```"):
        candidate = re.sub(r"^```(?:json)?\s*", "", candidate)
        candidate = re.sub(r"\s*```$", "", candidate)

    # Find the outermost JSON array
    arr_start = candidate.find("[")
    arr_end = candidate.rfind("]")
    if arr_start >= 0 and arr_end > arr_start:
        candidate = candidate[arr_start:arr_end + 1]

    # Try strict parse, then a permissive fallback
    try:
        data = json.loads(candidate)
    except json.JSONDecodeError:
        # Drop trailing commas inside arrays/objects — common LLM quirk
        candidate = re.sub(r",\s*([}\]])", r"\1", candidate)
        data = json.loads(candidate)

    if not isinstance(data, list):
        raise ValueError("Seed response is not a list")

    seeds = [_coerce_seed_item(item) for item in data if isinstance(item, dict)]
    # Pad/trim to expected count so the picker always has N cards (even
    # if the LLM returned fewer).
    while len(seeds) < expected:
        idx = len(seeds)
        seeds.append({
            "title": f"Seed {idx + 1}",
            "description": "Auto-generated fallback seed.",
            "style": "fantasy painterly",
            "tone": "dramatic",
            "characters": "",
        })
    return seeds[:expected]


@app.post("/api/seed-suggestions")
async def seed_suggestions(req: SeedRequest):
    """Return N distinct story seeds for the given concept.

    The LLM picks the seeds' style + tone per item, so the picker can
    show variety without locking the user into one combination.
    """
    concept = (req.concept or "").strip()
    if len(concept) < 10:
        raise HTTPException(status_code=400,
                            detail="Concept must be at least 10 characters.")
    count = max(1, min(6, int(req.count or 3)))

    sys.path.insert(0, str(Path(__file__).parent))
    # call_llm lives in generate_story.py; importing it here is fine because
    # generate_story.py is a sibling module with no side effects at import.
    from generate_story import call_llm

    user_prompt = f"""Concept: {concept}
Style baseline: {req.style}
Tone baseline: {req.tone}
Characters hint: {req.characters or "(none)"}

Generate exactly {count} distinct story seeds. Output ONLY the JSON array."""

    raw = call_llm(SEED_SYSTEM, user_prompt, temperature=0.9)
    if not raw:
        raise HTTPException(status_code=500,
                            detail="LLM did not return any seed suggestions.")

    try:
        seeds = _parse_seed_response(raw, count)
    except (json.JSONDecodeError, ValueError) as e:
        raise HTTPException(status_code=500,
                            detail=f"Could not parse seed response: {e}")

    return {"seeds": seeds, "style": req.style, "tone": req.tone}


async def _run_generation(task_id: str, req: GenerateRequest):
    """Run the generate_story.py pipeline in a subprocess and stream progress."""
    script = STORY_VIEWER_DIR / "generate_story.py"
    last_progress_error: str | None = None

    cmd = [
        sys.executable, str(script),
        "--concept", req.story_concept,
        "--scenes", str(req.num_scenes),
        "--images-per-scene", str(req.images_per_scene),
        "--style", req.style,
        "--tone", req.tone,
        "--voice", getattr(req, "voice_preset", "Dean"),
    ]
    if req.characters:
        cmd += ["--characters", req.characters]

    process = await asyncio.create_subprocess_exec(
        *cmd,
        stdout=asyncio.subprocess.PIPE,
        stderr=asyncio.subprocess.PIPE,
        env={**os.environ, "XIAOMI_API_KEY": _resolve_env_var("XIAOMI_API_KEY"),
             "XIAOMI_BASE_URL": _resolve_env_var("XIAOMI_BASE_URL", "https://token-plan-sgp.xiaomimimo.com/v1")},
    )

    # Read stdout line by line, parse progress markers
    async def read_stdout():
        while True:
            line = await process.stdout.readline()
            if not line:
                break
            text = line.decode("utf-8", errors="replace").strip()
            if not text:
                continue

            if text.startswith("__PROGRESS__:"):
                try:
                    data = json.loads(text[13:])
                    status = data.get("status", "running")
                    message = data.get("message", "")
                    progress = data.get("progress")

                    update = {
                        "id": task_id,
                        "status": status,
                        "message": message,
                    }
                    if progress is not None:
                        update["progress"] = progress
                    _generation_tasks[task_id].update(update)

                    # Push to all websocket clients
                    payload = {
                        "type": "task_update",
                        "task_id": task_id,
                        "status": status,
                        "message": message,
                    }
                    if progress is not None:
                        payload["progress"] = progress
                    if status == "error" and message:
                        last_progress_error = message

                    for ws in _websocket_clients[:]:
                        try:
                            await ws.send_json(payload)
                        except Exception:
                            pass
                except json.JSONDecodeError:
                    pass

            elif text.startswith("__RESULT__:"):
                try:
                    data = json.loads(text[11:])
                    _generation_tasks[task_id].update({
                        "status": "done",
                        "message": f"Complete: {data.get('title', 'Unknown')}",
                        "progress": 1.0,
                        "result": data,
                    })
                    # Reload story cache
                    global _stories_cache
                    _stories_cache = load_stories()
                except json.JSONDecodeError:
                    pass

    async def read_stderr():
        while True:
            line = await process.stderr.readline()
            if not line:
                break
            text = line.decode("utf-8", errors="replace").strip()
            if text and "RequestsDependencyWarning" not in text and "warnings.warn(" not in text:
                _generation_tasks[task_id].setdefault("errors", []).append(text)

    await asyncio.gather(read_stdout(), read_stderr())
    await process.wait()

    if process.returncode != 0:
        errors = _generation_tasks[task_id].get("errors", [])
        failure_detail = last_progress_error
        if not failure_detail:
            failure_detail = next(
                (line for line in reversed(errors)
                 if "RequestsDependencyWarning" not in line and "warnings.warn(" not in line),
                None,
            )
        if not failure_detail:
            failure_detail = errors[-1] if errors else "Unknown"
        _generation_tasks[task_id].update({
            "status": "error",
            "message": f"Failed with exit code {process.returncode}",
            "progress": 0,
            "error_detail": failure_detail,
        })
        payload = {
            "type": "task_update", "task_id": task_id,
            "status": "error", "message": f"Failed: {failure_detail}",
        }
        for ws in _websocket_clients[:]:
            try:
                await ws.send_json(payload)
            except Exception:
                pass


