async def _run_story_delete(task_id: str, story_id: str, story_dir: Path, backup: bool) -> None:
    """Run story deletion off the request path and stream task updates."""
    loop = asyncio.get_running_loop()
    sys.path.insert(0, str(Path(__file__).parent))
    from delete_story import delete_story_with_progress

    def _progress(event: dict) -> None:
        stage = event.get("stage", "delete")
        message = event.get("message", "")
        pct = event.get("progress", 0)
        report = event.get("report")
        _generation_tasks[task_id].update({
            "stage": stage,
            "progress": pct,
            "message": message,
            "report": report,
        })
        _broadcast_ws_json_from_thread(loop, {
            "type": "task_update",
            "task_id": task_id,
            "kind": "delete_story",
            "story_id": story_id,
            "stage": stage,
            "status": "error" if stage == "error" else "running",
            "progress": pct,
            "message": message,
            "report": report,
        })

    try:
        report = await loop.run_in_executor(
            None,
            lambda: delete_story_with_progress(
                story_dir,
                backup=backup,
                progress_callback=_progress,
            ),
        )

        global _stories_cache
        _generation_tasks[task_id]["report"] = report
        if report["errors"]:
            message = f"Delete partially failed: {report['errors']}"
            _generation_tasks[task_id].update({
                "status": "error",
                "stage": "error",
                "progress": 1.0,
                "message": message,
                "result": report,
            })
            await _broadcast_ws_json({
                "type": "task_update",
                "task_id": task_id,
                "kind": "delete_story",
                "story_id": story_id,
                "status": "error",
                "stage": "error",
                "progress": 1.0,
                "message": message,
                "result": report,
            })
            return

        _stories_cache = load_stories()
        message = (
            f"Deleted {story_id}: {report['files_deleted']} file(s), "
            f"{report['bytes_freed']} bytes freed"
        )
        _generation_tasks[task_id].update({
            "status": "done",
            "stage": "complete",
            "progress": 1.0,
            "message": message,
            "result": report,
        })
        await _broadcast_ws_json({
            "type": "task_update",
            "task_id": task_id,
            "kind": "delete_story",
            "story_id": story_id,
            "status": "done",
            "stage": "complete",
            "progress": 1.0,
            "message": message,
            "result": report,
        })
        await _broadcast_ws_json({
            "type": "story_deleted",
            "story_id": story_id,
            "task_id": task_id,
        })
    except Exception as e:
        message = f"Delete failed: {e}"
        _generation_tasks[task_id].update({
            "status": "error",
            "stage": "error",
            "progress": 1.0,
            "message": message,
        })
        await _broadcast_ws_json({
            "type": "task_update",
            "task_id": task_id,
            "kind": "delete_story",
            "story_id": story_id,
            "status": "error",
            "stage": "error",
            "progress": 1.0,
            "message": message,
        })


