# translate HTTP request/response into the action's data shape and
# forward WebSocket progress to the right-side progress panel.

class ExtendRequest(BaseModel):
    scenes: int = 5
    images_per_scene: Optional[int] = None
    voice: Optional[str] = None
    tone: Optional[str] = None


class RegenRequest(BaseModel):
    backup: bool = True
    dry_run: bool = False
    force: bool = False


